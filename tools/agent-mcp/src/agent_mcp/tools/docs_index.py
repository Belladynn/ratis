@dataclass
class _CorpusCache:
    entries: list[Entry] = field(default_factory=list)
    loaded_at: float = 0.0
    sources_signature: tuple[str, ...] = ()


_CORPUS_CACHE = _CorpusCache()


def _reset_corpus_cache_for_tests() -> None:
    """Drop the multi-source corpus cache. Mirrors :func:`_reset_cache_for_tests`."""
    _CORPUS_CACHE.entries = []
    _CORPUS_CACHE.loaded_at = 0.0
    _CORPUS_CACHE.sources_signature = ()


def load_corpus(
    sources: list[IndexSource] | None = None,
    *,
    force: bool = False,
) -> list[Entry]:
    """Load every enabled :class:`IndexSource` and return the flattened entry list.

    Args:
        sources : custom source list — defaults to :data:`DEFAULT_SOURCES`.
            Tests typically pass a synthetic subset.
        force : skip the TTL cache for this call.

    Order of returned entries follows the order of ``sources`` (and inside
    each source, the order the parser emits). Cache key includes the source
    names so swapping sources between calls correctly invalidates.
    """
    pool = list(sources) if sources is not None else list(DEFAULT_SOURCES)
    enabled = [s for s in pool if s.enabled]
    signature = tuple(s.name for s in enabled)
    now = time.monotonic()
    cached_fresh = (
        not force
        and _CORPUS_CACHE.entries
        and _CORPUS_CACHE.sources_signature == signature
        and (now - _CORPUS_CACHE.loaded_at) < CACHE_TTL_SECONDS
    )
    if cached_fresh:
        return _CORPUS_CACHE.entries

    out: list[Entry] = []
    for src in enabled:
        for path in _resolve_glob(src.glob_pattern):
            try:
                entries = src.parser(path)
            except Exception:  # noqa: S112 — one bad file must not poison the corpus.
                continue
            # Defensive : ensure the parser stamped the source name.
            for e in entries:
                if e.source != src.name:
                    e_dict = e.model_dump()
                    e_dict["source"] = src.name
                    out.append(Entry(**e_dict))
                else:
                    out.append(e)

    _CORPUS_CACHE.entries = out
    _CORPUS_CACHE.loaded_at = now
    _CORPUS_CACHE.sources_signature = signature
    return out
